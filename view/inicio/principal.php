  <h3 class="m_3" style="text-align: justify; color:white;">Importancia del manejo de residuos</h3>
  <p style="text-align: justify;">Desde el punto de vista ambiental y de salud pública, el manejo adecuado de los residuos en las etapas que siguen a su generación permite mitigar los impactos negativos sobre el ambiente, la salud y reducir la presión sobre los recursos naturales. El reuso y reciclaje de materiales son fundamentales para reducir la presión sobre los ecosistemas y otras fuentes de recursos de las que se extraen. Paralelamente disminuye el uso de energía y de agua necesarios para su extracción y procesamiento, así como la necesidad de espacio para disponer finalmente los residuos.</p>
  <br>
  <p style="text-align: justify;">El reciclaje pretende convertir algunos de los materiales que componen los residuos (entre los más importantes están el papel y cartón, el vidrio, algunos metales y el plástico) en materiales reusables en los procesos productivos. Desde el punto de vista de la gestión de los residuos, el reciclaje tiene la ventaja de reducir el volumen de materiales que requieren ser recolectados, transportados y dispuestos en sitios adecuados. La revalorización de los residuos también disminuye el consumo de materias primas, electricidad y agua, entre otros insumos, que serían necesarios para la extracción y procesamiento de nuevos materiales.</p>
  <p><i>Fuente: Enterate, febrero de 2019</i></p>
